#include <bits/stdc++.h>

int main() {
    std::ios_base::sync_with_stdio(false);
    std::cin.tie(nullptr);
    
    int tt; std::cin >> tt;
    while (tt--) {
        int n; std::cin >> n;

        int res = 0;
        for (int i = n - 1; i > 0; i -= 2) {
            res += i * 2;
        }

        std::cout << res / 2 + 1 << '\n';
    }
    return 0;
}
